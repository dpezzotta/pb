    static function normalize_course_name($course_name)
    {
        $name = trim((string)$course_name);
        $name = preg_replace('/^(STEM|TECH|SPECIAL)\s+[K0-9\-]+:\s*/i', '', $name);
        $name = preg_replace('/\s+/', ' ', $name);
        return trim($name);
    }

    static function entry_for_course($course_name)
    {
        $entries = self::entries();
        $name = self::normalize_course_name($course_name);
        if (isset($entries[$name])) return $entries[$name] + array('course' => $name);
        return array(
            'course' => $name ? $name : 'PlanetBravo Course',
            'title' => 'Next Steps After Camp',
            'body' => 'Most course files are uploaded to the camper PlanetBravo Google Drive by the end of the week. Log in at drive.planetbravo.com with the camper username and password provided by PlanetBravo. If you need help locating files for this course, please reply to your award email or contact info@planetbravo.com.',
            'links' => array('PlanetBravo Google Drive' => 'https://drive.planetbravo.com')
        );
    }

    static function all_entries()
    {
        $entries = self::entries();
        ksort($entries, SORT_NATURAL | SORT_FLAG_CASE);
        return $entries;
    }

    static function token_for_award($award_id)
    {
        $award_id = (int)$award_id;
        if ($award_id <= 0) return '';
        return $award_id . '.' . substr(hash_hmac('sha256', (string)$award_id, self::secret()), 0, 24);
    }

    static function award_id_from_token($token)
    {
        $parts = explode('.', (string)$token);
        if (count($parts) != 2) return 0;
        $award_id = (int)$parts[0];
        if ($award_id <= 0) return 0;
        $expected = substr(hash_hmac('sha256', (string)$award_id, self::secret()), 0, 24);
        return hash_equals($expected, $parts[1]) ? $award_id : 0;
    }

    static function url_for_award($award_id)
    {
        $token = self::token_for_award($award_id);
        return 'https://www.planetbravo.com/camps/nextsteps.php?t=' . rawurlencode($token);
    }

    static function secret()
    {
        if (defined('DB_PASSWORD')) return DB_PASSWORD . '|course-next-steps';
        return 'planetbravo-course-next-steps';
    }
